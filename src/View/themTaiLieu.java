    private void addTheLoaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTheLoaiActionPerformed
        // TODO add your handling code here:

        anDanhMuc();
        switch (addTheLoai.getSelectedItem().toString()) {
            case "Sách":
                addTenTG.setVisible(true);
                addTenTGField.setVisible(true);
                addSoTrangField.setVisible(true);
                addSoTrang.setVisible(true);
                break;
            case "Tạp Chí":
                addSoPhatHanh.setVisible(true);
                addSoPhatHanhField.setVisible(true);
                addThang.setVisible(true);
                addThangField1.setVisible(true);
                break;
            case "Báo":
                addThang.setVisible(true);
                addThangField1.setVisible(true);
                addNgay.setVisible(true);
                addNgayField.setVisible(true);
                break;

        }
    }//GEN-LAST:event_addTheLoaiActionPerformed

