    private void setTimKiemData() {

        addTheLoai.addItem("Chọn thể loại");
        for (String theLoai : taiLieuservice.getTheLoai()) {
            addTheLoai.addItem(theLoai);
        }
    }

    private void setNgayPhatHanh() {

        addNgay.removeAllItems();
        for (int i = 1; i <= taiLieuservice.getDate(Integer.valueOf(addThang.getSelectedItem().toString())); i++) {
            addNgay.addItem(String.valueOf(i));
        }
    }

