    private void setTableData(List<TaiLieu> taiLieus) {
        for (TaiLieu taiLieu : taiLieus) {
            defaultTableModel.addRow(new Object[]{taiLieu.getMaTaiLieu(), taiLieu.getLoaiTaiLieu(),
                taiLieu.getTenNhaXuatBan(), taiLieu.getSoBanPhatHanh()});
        }
    }

