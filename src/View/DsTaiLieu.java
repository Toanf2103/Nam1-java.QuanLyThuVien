    private void chiTietBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chiTietBTActionPerformed
        // TODO add your handling code here:
        if (tableData.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(DsTaiLieu.this, "Vui lòng chọn tài liệu xem", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } else {
            new chiTietTaiLieu(Integer.valueOf(String.valueOf(tableData.getValueAt(tableData.getSelectedRow(), 0)))).setVisible(true);
            this.dispose();
        }

    }//GEN-LAST:event_chiTietBTActionPerformed

    private void timKiemListItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_timKiemListItemStateChanged
        // TODO add your handling code here:
        //day;

        String tenTaiLieu = timKiemList.getSelectedItem().toString();
        if (tenTaiLieu.equals("Tất cả")) {
            defaultTableModel.setRowCount(0);
            setTableData(taiLieuservice.getAllTaiLieu());
        } else {
            defaultTableModel.setRowCount(0);
            
            setTableData(taiLieuservice.getAllTaiLieu(tenTaiLieu));
        }

    }//GEN-LAST:event_timKiemListItemStateChanged

    private void deleteBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBTActionPerformed
        // TODO add your handling code here:
        int row = tableData.getSelectedRow();
        System.out.println(row);
        if (row == -1) {

            JOptionPane.showMessageDialog(DsTaiLieu.this, "Vui lòng chọn tài liệu xóa", "Lỗi", JOptionPane.ERROR_MESSAGE);

        } else {
            int maTaiLieu = Integer.valueOf(String.valueOf(tableData.getValueAt(row, 0)));
            int confirm = JOptionPane.showConfirmDialog(DsTaiLieu.this, " Bạn chắc chắn muốn xóa ");
            if (confirm == JOptionPane.YES_OPTION) {

                if (taiLieuservice.deleteTaiLieu(maTaiLieu) == 0) {
                    JOptionPane.showMessageDialog(DsTaiLieu.this, "Xóa thất bại! Vui lòng thử lại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                } else {
                    defaultTableModel.setRowCount(0);
                    setTableData(taiLieuservice.getAllTaiLieu());
                }

            }
    }//GEN-LAST:event_deleteBTActionPerformed
    }
